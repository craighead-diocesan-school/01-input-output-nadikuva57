function madLibs() {
    let exclamation = prompt('Give me an exclamation {eg: WOW!')
    let adverb = prompt('Give me an adverb {eg: happily}')
    let noun = prompt('Give me a noun  {eg: School}')
    let adjective = prompt('Give me an adjective  {eg: ugly}')

    alert(+exclamation+ '! He said ' +adverb+ ' as he jumped into his convertible ' +noun+ ' and drove off with his ' +adjective+ ' wife ')
}